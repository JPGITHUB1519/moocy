    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DCurso curso_data = new DCurso();
        int remaining_selected_row = table_remaining_courses.getSelectedRow();
        if(remaining_selected_row != -1) {
            int curso_id = Integer.parseInt(table_remaining_courses.getModel().getValueAt(remaining_selected_row, 0).toString());
            curso_data.addCourseToUsuario(Session.user_id, curso_id);
            tabla_mis_cursos.setModel(curso_data.show_get_cursos_by_usuario(Session.user_id));
            table_remaining_courses.setModel(curso_data.show_remaining_cursos_by_usuario(Session.user_id));
            
        }
        
        //System.out.println(curso_id );
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DCurso curso_data = new DCurso();
        int mis_cursos_row = tabla_mis_cursos.getSelectedRow();
        if(mis_cursos_row != -1) {
            int curso_id = Integer.parseInt(tabla_mis_cursos.getModel().getValueAt(mis_cursos_row, 0).toString());
            curso_data.DeleteCourseToUsuario(Session.user_id, curso_id);
            tabla_mis_cursos.setModel(curso_data.show_get_cursos_by_usuario(Session.user_id));
            table_remaining_courses.setModel(curso_data.show_remaining_cursos_by_usuario(Session.user_id));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

