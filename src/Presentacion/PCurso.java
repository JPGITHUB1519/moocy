    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        boolean has_error = false;
        if(txtnombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Deberia Ingresar el nombre del curso");
            txtnombre.requestFocus();
            has_error = true;
        }

        if(txtslogan.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Deberia Ingresar el slogan del curso");
            txtslogan.requestFocus();
            has_error = true;
        }

        if(txtdescripcion.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Deberia Ingresar la descripcion del curso");
            txtdescripcion.requestFocus();
            has_error = true;
        }

        if(!has_error) {
            MCurso curso = new MCurso();
            DCurso curso_data = new DCurso();
            curso.setNombre(txtnombre.getText());
            curso.setSlogan(txtslogan.getText());

            int seleccionado = cboskill_level.getSelectedIndex();
            curso.setSkill_level((String) cboskill_level.getItemAt(seleccionado));

            curso.setDescripcion(txtslogan.getText());
            curso.setFecha_lanzamiento(txtfecha_lanzamiento.getText());
            curso.setIs_active(chkis_active.isSelected());


            if (this.accion.equals("guardar")) {
                if(curso_data.insert(curso) > 0) {
                    JOptionPane.showMessageDialog(rootPane, "El curso fue registrado Satisfactoriamiente");
                    this.mostrar("");
                    this.inhabilitar();
                }
            }
            else if(this.accion.equals("editar")) {
                System.out.println("Editar");
                curso.setId(Integer.parseInt(txtidcurso.getText()));
                if(curso_data.update(curso) > 0) {
                    JOptionPane.showMessageDialog(rootPane, "El curso fue editado exitosamente");
                    this.mostrar("");
                    this.inhabilitar();
                }
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

