    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidcurso.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Estas seguro que desea eliminar el curso", "Confimar", 2);

            if (confirmacion == 0) {
                MCurso curso = new MCurso();
                DCurso curso_data = new DCurso();

                curso.setId(Integer.parseInt(txtidcurso.getText()));
                curso_data.delete(curso);
                this.mostrar("");
                this.inhabilitar();
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

