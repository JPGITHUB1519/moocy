    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        // TODO add your handling code here:
        btnguardar.setText("Editar");
        this.habilitar();
        btneliminar.setEnabled(true);
        accion = "editar";

        int fila = tablalistado.rowAtPoint(evt.getPoint());
        txtidcurso.setText(tablalistado.getValueAt(fila, 0).toString());
        txtnombre.setText(tablalistado.getValueAt(fila, 1).toString());
        txtslogan.setText(tablalistado.getValueAt(fila, 2).toString());
        txtdescripcion.setText(tablalistado.getValueAt(fila, 3).toString());
        cboskill_level.setSelectedItem(tablalistado.getValueAt(fila, 4).toString());
        txtfecha_lanzamiento.setText(tablalistado.getValueAt(fila, 5).toString());
        chkis_active.setSelected(Boolean.parseBoolean(tablalistado.getValueAt(fila, 6).toString()));
    }//GEN-LAST:event_tablalistadoMouseClicked

